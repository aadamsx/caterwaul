d('|x| [x + 1]', 'alift')([1, 2, 3])            // => [2, 3, 4]
d('|x| [x, x + 1]', 'alift')([1, 2, 3])         // => [1, 2, 2, 3, 3, 4]
d('|x| x&1 && [x]', 'alift')([1, 2, 3, 4])      // => [1, 3]
d([0], 'alift')([1, 2, 3])                      // => [1, 2, 3]
d([0x71a, 0x8c], 'alift')([1, 2, 3])            // => [2, 1, 3, 4, 4, 9]
d(-0x8c, 'alift')([1, 2, 3])                    // => [1, 4, 9]
d([-0x8c], 'alift')([1, 2, 3])                  // => [[1], [4], [9]]